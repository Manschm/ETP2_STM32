void ah_motor(motor_cmd_t motor_cmd)
{
    if ((door_state != DOOR_LOCK) && (motor_cmd != MOTOR_OFF)) {
        ah_show_exception(ERROR, "Lock before moving!");
    }
    elevator_state = motor_cmd;
}